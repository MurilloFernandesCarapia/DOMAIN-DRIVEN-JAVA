package br.com.fiap;

public class Constante {
    public static void main(String[] args) {
        //declaração e inicializaçõa de constantes
        final double PI = 3.1415;
        final float GRAVIDADE_TERRESTRE = 9.8f;

        //Sequencia de Escape: \n (pula de linha)
        //Sequencia de Escape: \t (tabulação)
        System.out.println("Valor de PI:\t\t\t" + PI + "\n Gravidade da Terra:\t" + GRAVIDADE_TERRESTRE);
    }
}
